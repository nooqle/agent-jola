interface I18nContextValue {
  language: Language;
  t: (key: string, values?: Values) => string;
}

const I18nContext = createContext<I18nContextValue>({
  language: "zh",
  t: (key) => dictionaries.zh[key] ?? key
});

export function I18nProvider({ language, children }: { language: Language; children: ReactNode }) {
  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      t: (key, values) => format(dictionaries[language][key] ?? dictionaries.zh[key] ?? key, values)
    }),
    [language]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}

export function itemLabel(type: ItemType | undefined, language: Language): string {
  if (!type) return dictionaries[language]["item.unknown"] ?? "item";
  return dictionaries[language][`item.${type}`] ?? dictionaries.zh[`item.${type}`] ?? type;
}

export function itemHelp(type: ItemType, language: Language): string {
  return (
    dictionaries[language][`item.${type}.help`] ?? dictionaries.zh[`item.${type}.help`] ?? type
  );
}

export function itemGlyph(type: ItemType): string {
  const glyphs: Record<ItemType, string> = {
    rangeUp: "火",
    capacityUp: "+",
    speedUp: "速",
    shield: "盾",
    pierce: "穿",
    quickFuse: "急"
  };
  return glyphs[type];
}

export function actionLabel(
  type: string,
  direction: string | undefined,
  language: Language
): string {
  const action =
    dictionaries[language][`action.${type}`] ?? dictionaries.zh[`action.${type}`] ?? type;
  if (type !== "move" || !direction) return action;
  const dir = dictionaries[language][`direction.${direction}`] ?? direction;
  return language === "en" ? `${action} ${dir}` : `${action}${dir}`;
}

export function eventTitle(event: GameEvent, language: Language): string {
  return (
    dictionaries[language][`event.${event.type}`] ??
    dictionaries.zh[`event.${event.type}`] ??
    event.type
  );
}

export function eventDescription(event: GameEvent, language: Language): string {
  const agent = event.agentId ?? "Agent";
  const item = itemLabel(event.itemType, language);
  if (event.type === "finished" && event.agentId) {
    return format(
      dictionaries[language]["event.msg.finishedWin"] ??
        dictionaries.zh["event.msg.finishedWin"] ??
        "{agent}",
      {
        agent
      }
    );
  }
  const template = dictionaries[language][`event.msg.${event.type}`];
  if (!template) return event.message;
  return format(template, { agent, item });
}

function format(template: string, values: Values = {}): string {
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? ""));
}
